final class ConformanceFinding {
  const ConformanceFinding({
    required this.rule,
    required this.path,
    required this.line,
    required this.column,
    required this.evidence,
    required this.replacement,
    this.entity,
  });

  final String rule;
  final String path;
  final int line;
  final int column;
  final String evidence;
  final String replacement;
  final String? entity;

  Map<String, Object?> toJson() => {
    'rule': rule,
    'path': path,
    'line': line,
    'column': column,
    'evidence': evidence,
    'replacement': replacement,
    if (entity != null) 'entity': entity,
  };
}

final class ConformanceInventoryReport {
  const ConformanceInventoryReport({
    required this.graph,
    required this.findings,
  });

  final String graph;
  final List<ConformanceFinding> findings;

  String toMarkdown() {
    final byRule = <String, List<ConformanceFinding>>{
      for (final rule in nodusConformanceRules) rule.id: [],
    };
    for (final finding in findings) {
      byRule[finding.rule]!.add(finding);
    }
    final output = StringBuffer()
      ..writeln('# Nodus semantic conformance inventory')
      ..writeln()
      ..writeln('<!-- GENERATED by `dart run nodus inventory --write`. -->')
      ..writeln()
      ..writeln(
        'This deterministic report combines the resolved `$graph` graph '
        'metadata with analyzer ASTs. Findings are migration evidence, not '
        'architecture exceptions.',
      )
      ..writeln()
      ..writeln('## Summary')
      ..writeln()
      ..writeln('| Classification | Findings |')
      ..writeln('| --- | ---: |');
    for (final rule in nodusConformanceRules) {
      output.writeln(
        '| `${rule.id}` — ${_markdown(rule.title)} | '
        '${byRule[rule.id]!.length} |',
      );
    }
    output
      ..writeln()
      ..writeln('Total findings: **${findings.length}**.')
      ..writeln()
      ..writeln('## Evidence');
    if (findings.isEmpty) {
      output
        ..writeln()
        ..writeln('No semantic conformance findings.');
      return output.toString();
    }
    for (final rule in nodusConformanceRules) {
      final entries = byRule[rule.id]!;
      if (entries.isEmpty) continue;
      output
        ..writeln()
        ..writeln('### ${rule.title} (`${rule.id}`)')
        ..writeln()
        ..writeln('| Location | Entity | Evidence | Generated replacement |')
        ..writeln('| --- | --- | --- | --- |');
      for (final finding in entries) {
        output.writeln(
          '| `${_markdown(finding.path)}:${finding.line}:${finding.column}` '
          '| ${_markdown(finding.entity ?? '—')} '
          '| `${_markdown(finding.evidence)}` '
          '| ${_markdown(finding.replacement)} |',
        );
      }
    }
    return output.toString();
  }

  String toPrettyJson() {
    const encoder = JsonEncoder.withIndent('  ');
    final counts = {
      for (final rule in nodusConformanceRules)
        rule.id: findings.where((finding) => finding.rule == rule.id).length,
    };
    return '${encoder.convert({
      'version': 1,
      'graph': graph,
      'rules': [
        for (final rule in nodusConformanceRules) {'id': rule.id, 'title': rule.title, 'count': counts[rule.id]},
      ],
      'findings': [for (final finding in findings) finding.toJson()],
    })}\n';
  }
}

final class NodusConformanceInventory {
  NodusConformanceInventory({required Directory root}) : root = root.absolute;

  final Directory root;

  ConformanceInventoryReport scan() {
    final metadata = _GraphMetadata.read(
      File(
        path.join(root.path, 'lib', 'src', 'generated', 'nodus.explain.g.json'),
      ),
    );
    final findings = <ConformanceFinding>[];
    final files = _sourceFiles();
    for (final file in files) {
      final source = file.readAsStringSync();
      if (_isGeneratedSource(file, source)) continue;
      final parsed = parseString(
        content: source,
        path: file.path,
        throwIfDiagnostics: false,
      );
      final relative = path.relative(file.path, from: root.path);
      final context = _SourceContext(
        relativePath: relative,
        source: source,
        lineInfo: parsed.lineInfo,
      );
      parsed.unit.accept(
        _ConformanceVisitor(
          metadata: metadata,
          context: context,
          addFinding: findings.add,
        ),
      );
    }
    final ruleOrder = {
      for (var index = 0; index < nodusConformanceRules.length; index++)
        nodusConformanceRules[index].id: index,
    };
    findings.sort((left, right) {
      final byRule = ruleOrder[left.rule]!.compareTo(ruleOrder[right.rule]!);
      if (byRule != 0) return byRule;
      final byPath = left.path.compareTo(right.path);
      if (byPath != 0) return byPath;
      final byLine = left.line.compareTo(right.line);
      if (byLine != 0) return byLine;
      return left.column.compareTo(right.column);
    });
    return ConformanceInventoryReport(
      graph: metadata.graphName,
      findings: List.unmodifiable(findings),
    );
  }

  List<File> _sourceFiles() {
    final sourceRoot = Directory(path.join(root.path, 'lib'));
    if (!sourceRoot.existsSync()) return const [];
    final files = sourceRoot
        .listSync(recursive: true, followLinks: false)
        .whereType<File>()
        .where((file) => file.path.endsWith('.dart'))
        .toList(growable: false);
    files.sort((left, right) => left.path.compareTo(right.path));
    return files;
  }

  bool _isGeneratedSource(File file, String source) {
    final relative = path.relative(file.path, from: root.path);
    return relative.contains(
          '${path.separator}src${path.separator}generated',
        ) ||
        relative.endsWith('.g.dart') ||
        relative.endsWith('.freezed.dart') ||
        relative.endsWith('.entity.g.dart') ||
        source.startsWith('// GENERATED CODE');
  }
}

final class _GraphMetadata {
  const _GraphMetadata({required this.graphName, required this.entities});

  final String graphName;
  final List<_EntityMetadata> entities;

  Map<String, _EntityMetadata> get byName => {
    for (final entity in entities) entity.name: entity,
  };

  Map<String, _EntityMetadata> get byListName => {
    for (final entity in entities) entity.listName: entity,
  };

  static _GraphMetadata read(File file) {
    if (!file.existsSync()) {
      throw FormatException(
        'Missing generated graph explanation at ${file.path}.',
      );
    }
    final decoded = jsonDecode(file.readAsStringSync());
    if (decoded is! Map<String, Object?>) {
      throw FormatException('Invalid graph explanation at ${file.path}.');
    }
    final rawEntities = decoded['entities'];
    if (rawEntities is! List<Object?>) {
      throw FormatException('Graph explanation has no entity list.');
    }
    final entities = <_EntityMetadata>[];
    for (final rawEntity in rawEntities) {
      if (rawEntity is! Map<String, Object?>) {
        throw FormatException('Invalid entity graph explanation entry.');
      }
      final generated = rawEntity['generatedApi'];
      if (generated is! Map<String, Object?>) {
        throw FormatException(
          'Entity ${rawEntity['name']} has no generated API explanation.',
        );
      }
      final table = rawEntity['table']! as String;
      entities.add(
        _EntityMetadata(
          name: rawEntity['name']! as String,
          source: rawEntity['source']! as String,
          ownership: rawEntity['ownership']! as String,
          unbounded: rawEntity['cardinality'] == 'unbounded',
          setAccessor:
              generated['setAccessor'] as String? ?? _lowerCamel(table),
          listName: generated['list']! as String,
          capabilities: _enabledCapabilities(rawEntity['capabilities']),
          actionNames: {
            for (final action in (rawEntity['actions']! as List<Object?>))
              (action! as Map<String, Object?>)['name']! as String,
          },
        ),
      );
    }
    return _GraphMetadata(
      graphName: decoded['graph']! as String,
      entities: List.unmodifiable(entities),
    );
  }

  static Set<String> _enabledCapabilities(Object? value) {
    if (value is! Map<String, Object?>) return const {};
    return {
      for (final entry in value.entries)
        if (entry.value == true) entry.key,
    };
  }
}

