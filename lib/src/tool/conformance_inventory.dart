final class _MutationLoopFinder extends RecursiveAstVisitor<void> {
  _MutationLoopFinder(this.isMutation);

  final bool Function(MethodInvocation invocation) isMutation;
  final List<AstNode> loops = [];

  void search(AstNode node) => node.accept(this);

  @override
  void visitForStatement(ForStatement node) {
    if (_containsMutation(node.body)) loops.add(node);
    super.visitForStatement(node);
  }

  @override
  void visitMethodInvocation(MethodInvocation node) {
    if (const {'forEach', 'map'}.contains(node.methodName.name) &&
        node.argumentList.arguments.whereType<FunctionExpression>().any(
          (callback) => _containsMutation(callback.body),
        )) {
      loops.add(node);
    }
    super.visitMethodInvocation(node);
  }

  bool _containsMutation(AstNode node) {
    final finder = _MutationFinder(isMutation);
    node.accept(finder);
    return finder.found;
  }
}

final class _MutationFinder extends RecursiveAstVisitor<void> {
  _MutationFinder(this.isMutation);

  final bool Function(MethodInvocation invocation) isMutation;
  bool found = false;

  @override
  void visitMethodInvocation(MethodInvocation node) {
    if (isMutation(node)) found = true;
    if (!found) super.visitMethodInvocation(node);
  }
}

Expression? _singleExpression(FunctionBody body) {
  Expression? expression;
  if (body is ExpressionFunctionBody) {
    expression = body.expression;
  } else if (body is BlockFunctionBody && body.block.statements.length == 1) {
    final statement = body.block.statements.single;
    expression = switch (statement) {
      ExpressionStatement value => value.expression,
      ReturnStatement value => value.expression,
      _ => null,
    };
  }
  while (expression is AwaitExpression) {
    expression = expression.expression;
  }
  return expression;
}

bool _hasAnnotation(NodeList<Annotation> metadata, String name) => metadata.any(
  (annotation) => annotation.name.toSource().split('.').last == name,
);

bool _isStructuredType(String type) {
  final base = type.replaceFirst(RegExp(r'\?$'), '').split('<').first;
  return const {
    'List',
    'Set',
    'Map',
    'Iterable',
    'JsonMap',
    'Object',
    'dynamic',
  }.contains(base);
}

Set<String> _memberNames(ClassMember member) => switch (member) {
  FieldDeclaration field => {
    for (final variable in field.fields.variables) variable.name.lexeme,
  },
  MethodDeclaration method => {method.name.lexeme},
  _ => const {},
};

String _evidence(String source) {
  final compact = source.replaceAll(RegExp(r'\s+'), ' ').trim();
  if (compact.length <= 180) return compact;
  return '${compact.substring(0, 177)}...';
}

String _markdown(String source) =>
    source.replaceAll('|', r'\|').replaceAll('`', "'").replaceAll('\n', ' ');

String _lowerCamel(String snakeCase) {
  final parts = snakeCase.split('_');
  return parts.first +
      parts
          .skip(1)
          .map((part) => '${part[0].toUpperCase()}${part.substring(1)}')
          .join();
}

bool _containsAny(String source, Set<String> candidates) =>
    candidates.any(source.contains);

bool _isNamedArgumentSource(String source) =>
    RegExp(r'^[a-zA-Z_$][a-zA-Z0-9_$]*\s*:').hasMatch(source);

String _argumentValueSource(String source) {
  if (!_isNamedArgumentSource(source)) return source;
  return source.substring(source.indexOf(':') + 1).trim();
}

