final class _ConformanceVisitor extends RecursiveAstVisitor<void> {
  _ConformanceVisitor({
    required this.metadata,
    required this.context,
    required void Function(ConformanceFinding finding) addFinding,
  }) : _addFinding = addFinding,
       _entitiesByName = metadata.byName,
       _entitiesByListName = metadata.byListName,
       _mutationNames = {
         ..._conventionalMutationNames,
         for (final entity in metadata.entities) ...entity.actionNames,
       };

  final _GraphMetadata metadata;
  final _SourceContext context;
  final void Function(ConformanceFinding finding) _addFinding;
  final Map<String, _EntityMetadata> _entitiesByName;
  final Map<String, _EntityMetadata> _entitiesByListName;
  final Set<String> _mutationNames;
  final Set<String> _findingKeys = {};

  _EntityMetadata? _currentEntity;
  String? _currentExtensionType;
  Map<String, Set<_EntityMetadata>> _localBindings = const {};

  @override
  void visitClassDeclaration(ClassDeclaration node) {
    final previous = _currentEntity;
    final className = node.namePart.typeName.lexeme;
    _currentEntity = _entitiesByName[className];
    if (_currentEntity != null || _hasAnnotation(node.metadata, 'Entity')) {
      _inspectEntityDeclaration(node, _currentEntity);
    }
    super.visitClassDeclaration(node);
    _currentEntity = previous;
  }

  @override
  void visitExtensionDeclaration(ExtensionDeclaration node) {
    final previous = _currentExtensionType;
    _currentExtensionType = node.onClause?.extendedType.toSource();
    super.visitExtensionDeclaration(node);
    _currentExtensionType = previous;
  }

  @override
  void visitMethodDeclaration(MethodDeclaration node) {
    final previous = _localBindings;
    _localBindings = _collectLocalBindings(node.body, previous);
    _inspectRawIdentityGetter(node);
    _inspectGraphForwarder(node);
    super.visitMethodDeclaration(node);
    _localBindings = previous;
  }

  @override
  void visitFunctionDeclaration(FunctionDeclaration node) {
    final previous = _localBindings;
    _localBindings = _collectLocalBindings(
      node.functionExpression.body,
      previous,
    );
    super.visitFunctionDeclaration(node);
    _localBindings = previous;
  }

  @override
  void visitArgumentList(ArgumentList node) {
    for (final argument in node.arguments) {
      if (RegExp(r'^pageSize\s*:\s*1$').hasMatch(argument.toSource())) {
        _finding(
          'exact-one-query',
          node.parent ?? node,
          entity: _singleEntityFromSource(node.parent?.toSource() ?? ''),
        );
      }
    }
    super.visitArgumentList(node);
  }

  @override
  void visitInstanceCreationExpression(InstanceCreationExpression node) {
    final constructor = node.constructorName.name?.name;
    if (constructor == 'forOwner') {
      _inspectCurrentOwner(
        node,
        node.constructorName.type.toSource(),
        node.argumentList,
      );
    }
    super.visitInstanceCreationExpression(node);
  }

  @override
  void visitMethodInvocation(MethodInvocation node) {
    final name = node.methodName.name;
    if (name == 'forOwner') {
      _inspectCurrentOwner(node, node.target?.toSource(), node.argumentList);
    }
    if (name == 'transaction') _inspectTransaction(node);
    if (name == 'useAll') _inspectUseAll(node);
    if (name == 'now' && node.target?.toSource() == 'DateTime') {
      final domainPath = context.relativePath.contains('/domain/');
      final aggregateBoundary =
          _currentEntity != null ||
          _currentExtensionType == metadata.graphName ||
          _entitiesByName.containsKey(_currentExtensionType);
      if (domainPath || aggregateBoundary) {
        _finding('ambient-domain-clock', node, entity: _currentEntity);
      }
    }
    _inspectSuccessLog(node);
    super.visitMethodInvocation(node);
  }

  @override
  void visitSimpleIdentifier(SimpleIdentifier node) {
    if (node.name == 'itemSnapshots' &&
        (node.parent is PropertyAccess || node.parent is PrefixedIdentifier)) {
      _finding('legacy-item-snapshots', node, entity: _currentEntity);
    }
    super.visitSimpleIdentifier(node);
  }

  void _inspectEntityDeclaration(
    ClassDeclaration node,
    _EntityMetadata? entity,
  ) {
    final interfaces = {
      for (final type
          in node.implementsClause?.interfaces ?? const <NamedType>[])
        type.toSource().split('<').first,
    };
    final repeatedMembers = <String>{};
    for (final member in node.body.members) {
      if (member is FieldDeclaration) {
        if (member.isStatic) continue;
        final type = member.fields.type?.toSource() ?? '';
        for (final variable in member.fields.variables) {
          if (!member.fields.isFinal && !member.fields.isConst) {
            _finding('mutable-persisted-field', variable, entity: entity);
          }
          if (_isStructuredType(type)) {
            _finding('structured-persisted-field', variable, entity: entity);
          }
          repeatedMembers.add(variable.name.lexeme);
        }
      } else if (member is MethodDeclaration) {
        repeatedMembers.add(member.name.lexeme);
      }
    }
    for (final interface in interfaces) {
      final ownedMembers = _capabilityMembers[interface];
      if (ownedMembers == null) continue;
      for (final name in repeatedMembers.intersection(ownedMembers)) {
        final member = node.body.members.firstWhere(
          (candidate) => _memberNames(candidate).contains(name),
        );
        _finding(
          'manual-capability-member',
          member,
          entity: entity,
          replacement:
              'Remove `$name`; `$interface` supplies the complete generated '
              'capability.',
        );
      }
    }
  }

  void _inspectRawIdentityGetter(MethodDeclaration node) {
    if (!node.isGetter ||
        node.returnType?.toSource().startsWith('String') != true) {
      return;
    }
    final expression = _singleExpression(node.body)?.toSource();
    if (expression == null ||
        !RegExp(r'\b(?:\w+Id|\w+\.id)\.value\b').hasMatch(expression)) {
      return;
    }
    _finding('raw-identity-bridge', node, entity: _currentEntity);
  }

  void _inspectGraphForwarder(MethodDeclaration node) {
    if (_currentExtensionType != metadata.graphName || node.isGetter) return;
    final expression = _singleExpression(node.body);
    if (expression == null || !_isForwardingExpression(node, expression)) {
      return;
    }
    _finding(
      'graph-forwarding-wrapper',
      node,
      entity: _singleEntityFromSource(expression.toSource()),
    );
  }

  bool _isForwardingExpression(
    MethodDeclaration method,
    Expression expression,
  ) {
    final parameters = {
      for (final parameter
          in method.parameters?.parameters ?? const <FormalParameter>[])
        if (parameter.name != null) parameter.name!.lexeme,
    };
    final arguments = switch (expression) {
      MethodInvocation invocation => invocation.argumentList.arguments,
      InstanceCreationExpression creation => creation.argumentList.arguments,
      _ => null,
    };
    if (arguments == null) return false;
    for (final argument in arguments) {
      final source = _argumentValueSource(argument.toSource());
      if (!parameters.contains(source) &&
          source != 'this' &&
          source != 'accountId' &&
          source != 'entityGraph' &&
          !source.endsWith('.accountId')) {
        return false;
      }
    }
    if (expression is InstanceCreationExpression) {
      final type = expression.constructorName.type.toSource();
      return type.endsWith('List') || type.endsWith('Lookup');
    }
    final invocation = expression as MethodInvocation;
    final target = invocation.target?.toSource() ?? '';
    return parameters.contains(invocation.methodName.name) ||
        _mutationNames.contains(invocation.methodName.name) ||
        invocation.methodName.name.startsWith('lookup') ||
        invocation.methodName.name.startsWith('require') ||
        invocation.methodName.name.startsWith('by') ||
        _entitiesFromSource(target).isNotEmpty ||
        target.endsWith('List') ||
        target.endsWith('Lookup');
  }

  void _inspectCurrentOwner(
    AstNode node,
    String? listSource,
    ArgumentList arguments,
  ) {
    final entity = listSource == null
        ? null
        : _entitiesByListName[listSource.split('<').first];
    if (entity == null || entity.ownership != 'separate') return;
    final positional = [
      for (final argument in arguments.arguments)
        if (!_isNamedArgumentSource(argument.toSource())) argument,
    ];
    if (positional.length < 2) return;
    final graph = positional.first.toSource();
    final owner = positional[1].toSource();
    final isCurrentOwner =
        owner == 'accountId' ||
        owner == '$graph.accountId' ||
        (graph == 'this' && owner == 'this.accountId');
    if (!isCurrentOwner) return;
    _finding(
      'repeated-current-owner',
      node,
      entity: entity,
      replacement: '${entity.listName}.owned($graph)',
    );
  }

  void _inspectTransaction(MethodInvocation node) {
    final callback = node.argumentList.arguments
        .whereType<FunctionExpression>()
        .firstOrNull;
    if (callback == null) return;
    final expression = _singleExpression(callback.body);
    if (expression is! MethodInvocation || !_isMutation(expression)) return;
    _finding(
      'sole-mutation-transaction',
      node,
      entity: _singleEntityFromSource(expression.toSource()),
    );
  }

  void _inspectUseAll(MethodInvocation node) {
    final entities = _entitiesFromExpression(node.target);
    final unbounded = entities.where((entity) => entity.unbounded).toSet();
    if (unbounded.isEmpty) return;
    final entity = unbounded.length == 1 ? unbounded.single : null;
    _finding('unbounded-use-all', node, entity: entity);
    final callback = node.argumentList.arguments
        .whereType<FunctionExpression>()
        .firstOrNull;
    if (callback == null) return;
    final bulk = _MutationLoopFinder(_isMutation)..search(callback.body);
    for (final loop in bulk.loops) {
      _finding('client-bulk-loop', loop, entity: entity);
    }
  }

  void _inspectSuccessLog(MethodInvocation node) {
    if (!const {
      'debug',
      'fine',
      'finer',
      'finest',
      'info',
      'trace',
    }.contains(node.methodName.name)) {
      return;
    }
    final target = (node.target?.toSource() ?? '').toLowerCase();
    if (!target.contains('log')) return;
    final message = node.argumentList.arguments
        .whereType<StringLiteral>()
        .map((literal) => literal.stringValue)
        .whereType<String>()
        .join(' ')
        .toLowerCase();
    if (message.isEmpty ||
        !_containsAny(message, _successWords) ||
        !_containsAny(message, _persistenceWords)) {
      return;
    }
    final featurePath =
        context.relativePath.contains('/features/') ||
        context.relativePath.contains('/domain/') ||
        context.relativePath.contains('/application/');
    if (featurePath) {
      _finding('persistence-success-log', node, entity: _currentEntity);
    }
  }

  Map<String, Set<_EntityMetadata>> _collectLocalBindings(
    FunctionBody body,
    Map<String, Set<_EntityMetadata>> inherited,
  ) {
    final declarations = <VariableDeclaration>[];
    body.accept(_VariableCollector(declarations.add));
    final bindings = <String, Set<_EntityMetadata>>{
      for (final entry in inherited.entries) entry.key: {...entry.value},
    };
    for (var pass = 0; pass < 2; pass++) {
      for (final declaration in declarations) {
        final initializer = declaration.initializer;
        if (initializer == null) continue;
        final entities = _entitiesFromExpression(
          initializer,
          bindings: bindings,
        );
        if (entities.isNotEmpty) bindings[declaration.name.lexeme] = entities;
      }
    }
    return bindings;
  }

  Set<_EntityMetadata> _entitiesFromExpression(
    Expression? expression, {
    Map<String, Set<_EntityMetadata>>? bindings,
  }) {
    if (expression == null) return const {};
    final activeBindings = bindings ?? _localBindings;
    final result = _entitiesFromSource(expression.toSource());
    final identifiers = <String>[];
    expression.accept(_IdentifierCollector(identifiers.add));
    for (final identifier in identifiers) {
      result.addAll(activeBindings[identifier] ?? const {});
    }
    return result;
  }

  Set<_EntityMetadata> _entitiesFromSource(String source) {
    final result = <_EntityMetadata>{};
    for (final entity in metadata.entities) {
      if (RegExp('\\b${RegExp.escape(entity.listName)}\\b').hasMatch(source) ||
          RegExp(
            '\\.${RegExp.escape(entity.setAccessor)}\\b',
          ).hasMatch(source)) {
        result.add(entity);
      }
    }
    return result;
  }

  _EntityMetadata? _singleEntityFromSource(String source) {
    final entities = _entitiesFromSource(source);
    return entities.length == 1 ? entities.single : null;
  }

  bool _isMutation(MethodInvocation invocation) {
    final name = invocation.methodName.name;
    return _mutationNames.contains(name) ||
        _mutationPrefixes.any(name.startsWith);
  }

  void _finding(
    String rule,
    AstNode node, {
    _EntityMetadata? entity,
    String? replacement,
  }) {
    final location = context.lineInfo.getLocation(node.offset);
    final key = '$rule:${context.relativePath}:${node.offset}';
    if (!_findingKeys.add(key)) return;
    final descriptor = nodusConformanceRules.singleWhere(
      (candidate) => candidate.id == rule,
    );
    _addFinding(
      ConformanceFinding(
        rule: rule,
        path: context.relativePath,
        line: location.lineNumber,
        column: location.columnNumber,
        evidence: _evidence(node.toSource()),
        replacement: replacement ?? descriptor.replacement,
        entity: entity?.name ?? _currentEntity?.name,
      ),
    );
  }
}

